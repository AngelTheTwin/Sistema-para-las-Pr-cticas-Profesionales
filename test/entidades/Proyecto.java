package entidades;

/**
 *
 * @author angel
 */
public class Proyecto {
    private String nombreProyecto;
    private String descripcionProyecto;
    private String recursoProyecto;
    private int duracionProyecto;
    private String objetivoProyecto;
    private String metodologiaProyecto;

    public String getNombreProyecto() {
        return nombreProyecto;
    }

    public void setNombreProyecto(String nombreProyecto) {
        this.nombreProyecto = nombreProyecto;
    }

    public String getDescripcionProyecto() {
        return descripcionProyecto;
    }

    public void setDescripcionProyecto(String descripcionProyecto) {
        this.descripcionProyecto = descripcionProyecto;
    }

    public String getRecursoProyecto() {
        return recursoProyecto;
    }

    public void setRecursoProyecto(String recursoProyecto) {
        this.recursoProyecto = recursoProyecto;
    }

    public int getDuracionProyecto() {
        return duracionProyecto;
    }

    public void setDuracionProyecto(int duracionProyecto) {
        this.duracionProyecto = duracionProyecto;
    }

    public String getObjetivoProyecto() {
        return objetivoProyecto;
    }

    public void setObjetivoProyecto(String objetivoProyecto) {
        this.objetivoProyecto = objetivoProyecto;
    }

    public String getMetodologiaProyecto() {
        return metodologiaProyecto;
    }

    public void setMetodologiaProyecto(String metodologiaProyecto) {
        this.metodologiaProyecto = metodologiaProyecto;
    }

 
}
